    private boolean consultarUnidades(JTextField Nombre){
        try{
            encontrado = Proyecto.buscarPlatoPorNombre(Nombre.getText());
           if (encontrado)
           {
               // Plato encontrado
               String UnidadesPlato = String.valueOf(Proyecto.getUnidadesPlato());
                BCambiarUnidades.setEnabled(true);
                tfNombreCU.setForeground(Color.black);
                 // Datos del plato
                tfUnidadesApuntadas.setText(UnidadesPlato);
               return true;
           }
           else
           {
               // Plato nuevo
               platoNuevo();
               return true;
           }
       }
       catch(Exception e){
           System.out.println(e.getClass());
       }
        return false;
    }
    
    private void cambiarUnidadesPlato(JTextField Nombre, JTextField Unidades){
       boolean cambiarUnidades;
       
        try {
            if(Unidades.getText().isEmpty()){
                JActualizado1.setVisible(true);
            }
            int UnidadesActuales = Integer.parseInt(Unidades.getText());
            if(UnidadesActuales<=999 && UnidadesActuales>0){
                cambiarUnidades = Proyecto.cambiarUnidadesPlato(Nombre.getText(),UnidadesActuales);
                if(cambiarUnidades){
                    JActualizado.setVisible(true);
                    tfNombreCU.setText("");
                    tfNombreCU.setForeground(Color.black);
                    tfUnidadesActuales.setText("");
                    tfUnidadesApuntadas.setText("");
                    tfUnidadesActuales.setEnabled(false);
                    tfUnidadesActuales.setForeground(Color.black);
                    tfUnidadesApuntadas.setForeground(Color.black);
                    JActualizado1.setVisible(false);
                }
                else{
                    JActualizado.setVisible(false);
                    JActualizado1.setVisible(true);
                }
            }
            else{
                JActualizado.setVisible(false);
                JActualizado1.setVisible(true);
            }
            
        } catch (Exception e) {
            System.out.println("No se ha podido insertar" + e.getClass());
        }
    }
    
