    private void BValidarCUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BValidarCUActionPerformed
        try {
            boolean consultaNombre = consultaNombre(tfNombreCU);
            if(consultaNombre){
                tfNombreCU.setForeground(Color.red);
                tfNombreCU.setText("No existe");
                tfUnidadesApuntadas.setText("");
            }
            else{
                tfNombreCU.setForeground(Color.green);
                consultarUnidades(tfNombreCU);
                tfUnidadesActuales.setEnabled(true);
                
            }
            
            boolean validarUnidades = validarUnidades(tfUnidadesApuntadas);
            if(!validarUnidades){
                tfUnidadesApuntadas.setForeground(Color.red);
            }
            
            if(!consultaNombre && validarUnidades){
                BCambiarUnidades.setEnabled(true);
                Proyecto.mostrarUnidades(tfNombreCU.getText());
            }
            else{
                BCambiarUnidades.setEnabled(false);
            }
            
            
        } catch (Exception e) {
            System.out.println(e.getClass());
        }
        
    }//GEN-LAST:event_BValidarCUActionPerformed

