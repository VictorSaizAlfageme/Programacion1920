     private boolean validarNombre(JTextField Nombre) throws Exception{
        String nombrepat = "^[A-zÑñ\\s]{1,30}$";
        if (Pattern.matches(nombrepat, Nombre.getText())== false ){
            return false;
        }
        else{
            return true;
        }
    }
     
      private boolean validarUnidades(JTextField Unidades) throws Exception{
        String unidadespat = "^\\d{1,3}$";
        if (Pattern.matches(unidadespat, Unidades.getText())== false ){
            Unidades.setForeground(Color.red);
            return false;
        }
        else{
            Unidades.setForeground(Color.green);
            return true;
        }
    }
     
