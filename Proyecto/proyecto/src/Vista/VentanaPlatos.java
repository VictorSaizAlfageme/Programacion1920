    private void BValidarAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BValidarAActionPerformed
        try {
            boolean unidadesOK = validarUnidades(tfUnidadesA);
            consultarNombre();
            boolean nombreOK = consultaNombre(tfNombreA);
            
            if(nombreOK==false){
                
                BAñadir.setEnabled(false);
                tfUnidadesA.setForeground(Color.black);
                tfNombreA.setForeground(Color.black);
               // Datos del plato
                String UnidadesPlato = String.valueOf(Proyecto.getUnidadesPlato());
                tfUnidadesA.setText(UnidadesPlato);
            }
            else{
                BAñadir.setEnabled(false);
            }
            
            if(tfUnidadesA.getText().isEmpty()){
                tfUnidadesA.setText("Debes insertar algun valor");
            }
            
            if(!tfUnidadesA.getText().isEmpty() && unidadesOK==true && nombreOK==true){
                BAñadir.setEnabled(true);
            }
            else{
                BAñadir.setEnabled(false);
            }
        
        } catch (Exception e) {
            System.out.println(e.getClass());
        }
        
    }//GEN-LAST:event_BValidarAActionPerformed

